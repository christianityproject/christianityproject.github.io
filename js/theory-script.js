function darkTheme() {
  $(".adaptiveColor").css("color", "white");
  $("body").css("background-color", "#222222");
  $(".tableStripes").css("background-color", "#333333");
}
function blackTheme() {
  $(".adaptiveColor").css("color", "white");
  $("body").css("background-color", "black");
  $(".tableStripes").css("background-color", "#111111");
}
function whiteTheme() {
  $(".adaptiveColor").css("color", "black");
  $("body").css("background-color", "white");
  $(".tableStripes").css("background-color", "#eeeeee");
}
