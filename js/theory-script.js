function darkTheme() {
  $(".adaptiveColor").css("color", "white");
  $("body").css("background-color", "#222222");
}
function blackTheme() {
  $(".adaptiveColor").css("color", "white");
  $("body").css("background-color", "black");
}
function whiteTheme() {
  $(".adaptiveColor").css("color", "black");
  $("body").css("background-color", "white");
}
